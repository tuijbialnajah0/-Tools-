-- Trigger to run the function on every signup
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- 7. Tools Policies
CREATE POLICY "Tools are viewable by everyone." ON public.tools
    FOR SELECT USING (true);

CREATE POLICY "Only admins can modify tools." ON public.tools
    FOR ALL USING (
        EXISTS (
            SELECT 1 FROM public.profiles
            WHERE id = auth.uid() AND role = 'admin'
        )
    );

-- 7. Credit Transactions Policies
CREATE POLICY "Users can view own transactions." ON public.credit_transactions
    FOR SELECT USING (auth.uid() = user_id);

CREATE POLICY "Only admins can view all transactions." ON public.credit_transactions
    FOR SELECT USING (
        EXISTS (
            SELECT 1 FROM public.profiles
            WHERE id = auth.uid() AND role = 'admin'
        )
    );

-- 8. RPC Functions

-- Drop existing functions first to avoid return type change errors
DROP FUNCTION IF EXISTS public.admin_update_credits(UUID, INTEGER, TEXT, TEXT);
DROP FUNCTION IF EXISTS public.execute_tool(BIGINT);

-- Function to update user credits (Admin only)
CREATE OR REPLACE FUNCTION public.admin_update_credits(
    p_user_id UUID,
    p_amount INTEGER,
    p_action TEXT,
    p_reason TEXT DEFAULT 'Admin adjustment'
)
RETURNS VOID AS $$
DECLARE
    v_current_balance INTEGER;
    v_new_balance INTEGER;
BEGIN
    -- Check if caller is admin
    IF NOT EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin') THEN
        RAISE EXCEPTION 'Unauthorized: Only admins can update credits.';
    END IF;

    -- Get current balance
    SELECT credit_balance INTO v_current_balance FROM public.profiles WHERE id = p_user_id;

    -- Calculate new balance
    IF p_action = 'add' THEN
        v_new_balance := v_current_balance + p_amount;
    ELSIF p_action = 'deduct' THEN
        v_new_balance := v_current_balance - p_amount;
    ELSIF p_action = 'set' THEN
        v_new_balance := p_amount;
    ELSE
        RAISE EXCEPTION 'Invalid action: %', p_action;
    END IF;

    -- Update profile
    UPDATE public.profiles
    SET credit_balance = v_new_balance
    WHERE id = p_user_id;

    -- Log transaction
    INSERT INTO public.credit_transactions (user_id, amount, action, reason)
    VALUES (p_user_id, p_amount, p_action, p_reason);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to execute a tool (Deducts credits)
CREATE OR REPLACE FUNCTION public.execute_tool(p_tool_id BIGINT)
RETURNS JSON AS $$
DECLARE
    v_cost INTEGER;
    v_balance INTEGER;
    v_tool_name TEXT;
    v_is_admin BOOLEAN;
BEGIN
    -- Check if user is admin
    SELECT role = 'admin' INTO v_is_admin FROM public.profiles WHERE id = auth.uid();

    -- Get tool info
    SELECT tool_name, credit_cost INTO v_tool_name, v_cost FROM public.tools WHERE id = p_tool_id AND enabled = true;
    
    IF v_tool_name IS NULL THEN
        RAISE EXCEPTION 'Tool not found or disabled.';
    END IF;

    -- If not admin, check and deduct credits
    IF NOT v_is_admin THEN
        SELECT credit_balance INTO v_balance FROM public.profiles WHERE id = auth.uid();
        
        IF v_balance < v_cost THEN
            RAISE EXCEPTION 'Insufficient credits.';
        END IF;

        -- Deduct credits
        UPDATE public.profiles
        SET credit_balance = v_balance - v_cost
        WHERE id = auth.uid();

        -- Log transaction
        INSERT INTO public.credit_transactions (user_id, amount, action, reason)
        VALUES (auth.uid(), v_cost, 'deduct', 'Executed tool: ' || v_tool_name);
    END IF;

    RETURN json_build_object(
        'status', 'success',
        'message', 'Tool ' || v_tool_name || ' executed successfully!',
        'cost', CASE WHEN v_is_admin THEN 0 ELSE v_cost END
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

