-- 6. Secure RPC for executing tools (deducts credits)
CREATE OR REPLACE FUNCTION public.execute_tool(p_tool_id BIGINT)
RETURNS JSON AS $$
DECLARE
  v_user_id UUID;
  v_tool RECORD;
  v_balance INTEGER;
BEGIN
  v_user_id := auth.uid();
  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  SELECT * INTO v_tool FROM public.tools WHERE id = p_tool_id AND enabled = true;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Tool not found or disabled';
  END IF;

  SELECT credit_balance INTO v_balance FROM public.profiles WHERE id = v_user_id;
  IF v_balance < v_tool.credit_cost THEN
    RAISE EXCEPTION 'Insufficient credits';
  END IF;

  -- Deduct credits and update total spent
  UPDATE public.profiles 
  SET 
    credit_balance = credit_balance - v_tool.credit_cost,
    total_spent = total_spent + v_tool.credit_cost
  WHERE id = v_user_id;
  
  -- Log usage
  INSERT INTO public.tool_usage (user_id, tool_id, credits_spent) VALUES (v_user_id, p_tool_id, v_tool.credit_cost);
  
  -- Log history
  INSERT INTO public.credit_history (user_id, amount, type, reason) VALUES (v_user_id, v_tool.credit_cost, 'deduct', 'Used tool: ' || v_tool.tool_name);

  RETURN json_build_object('message', 'Successfully executed ' || v_tool.tool_name, 'result', 'Mock result for ' || v_tool.tool_name);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 6. Secure RPC for admins to update credits
CREATE OR REPLACE FUNCTION public.admin_update_credits(p_user_id UUID, p_amount INTEGER, p_action TEXT, p_reason TEXT)
RETURNS JSON AS $$
DECLARE
  v_admin_role TEXT;
  v_current_balance INTEGER;
  v_new_balance INTEGER;
  v_diff INTEGER;
  v_type TEXT;
BEGIN
  SELECT role INTO v_admin_role FROM public.profiles WHERE id = auth.uid();
  IF v_admin_role != 'admin' THEN
    RAISE EXCEPTION 'Admin access required';
  END IF;

  SELECT credit_balance INTO v_current_balance FROM public.profiles WHERE id = p_user_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'User not found';
  END IF;

  IF p_action = 'add' THEN
    v_new_balance := v_current_balance + p_amount;
    v_diff := p_amount;
    v_type := 'add';
  ELSIF p_action = 'deduct' THEN
    v_new_balance := GREATEST(0, v_current_balance - p_amount);
    v_diff := v_current_balance - v_new_balance;
    v_type := 'deduct';
  ELSIF p_action = 'set' THEN
    v_new_balance := p_amount;
    v_diff := ABS(p_amount - v_current_balance);
    IF p_amount > v_current_balance THEN
      v_type := 'add';
    ELSE
      v_type := 'deduct';
    END IF;
  ELSE
    RAISE EXCEPTION 'Invalid action';
  END IF;

  UPDATE public.profiles SET credit_balance = v_new_balance WHERE id = p_user_id;
  
  IF v_diff > 0 THEN
    INSERT INTO public.credit_history (user_id, amount, type, reason) VALUES (p_user_id, v_diff, v_type, COALESCE(p_reason, 'Admin adjustment'));
  END IF;

  RETURN json_build_object('message', 'Credits updated successfully', 'newBalance', v_new_balance);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

